package org.example;

public abstract class Shape {

    private int x;
    private int y;

    public Shape(){}

    public Shape(
            int x,
            int y
    ){
        this.x= x;
        this.y=y;
    }

    //setter
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }

    //getter
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }

    //methods :
    public abstract double calculateArea();
    public abstract double calculateCircumference();
}
